public class operaciones {

    public  static double raiz(double numero){
        double r;
        r=Math.sqrt(numero);
        return r;
    }
    public  static  double cuadrado(double numero){
        double c;
        c=Math.pow(numero,2);
        return c;
    }
    public static double  cubo(double numero){
        double cc;
        cc=Math.pow(numero,3);
        return cc;
    }
}
